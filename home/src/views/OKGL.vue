<h4>OKGL Mission, Participants</h4>
<p>EDMC recognizes the urgent need to bring this new architecture and approach to its members. In consultations with members, we have launched the Open Knowledge Graph Lab (OKGL). OKGL mission is to:</p>
<ul>
    <li>(1) Establish and grow a global community of Knowledge Graph Consumers, makers of technology, enablers such as management consultancies and Systems Integrators and industry standards bodies to promote enterprise-wide adoption of this technology.</li>
