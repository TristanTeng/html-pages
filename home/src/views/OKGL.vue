<h5>Phase 1 – Training, Ontology Review, POV on Graph Architectures</h5>
<p>OKGL is already serving early-adopter customers with Knowledge Graph training, Ontology reviews and ability to further refine domain specific Ontologies. Where applicable, OKGL is also leveraging its FIBO Ontology for broader Fraud and Risk use cases.</p>

<h5>Phase 2 – Mutualized value Knowledge Graph development and socialization</h5> 
<p>Establish working group to identify and develop 1st mutualized value Knowledge Graph use case. Early candidates include AML, Fraud leveraging FIBO standard. This working group will also develop the prototype to demonstrate how AML Knowledge Graph will help address the challenges in this space more efficiently than current techniques.</p>

<h5>Phase 3 – Additional Knowledge Graphs, Cloud Sandbox availability</h5>
<p>Due to intense focus on Covid-19 crisis, 2nd working group will focus on developing Covid-19 Knowledge Graph. Additional work groups on use cases such as supply-chain and workplace and worker safety are some additional candidates.
